                <h4 class="subHeading">Création d'une plateforme dédiée à l'intrapreneuriat</h4>
                <p class="souligne">Contexte:</p>
          Depuis 2017, je suis membre du « Shadow Comex » de Prisma Media ( 7 membres sélectionnés parmi les 1500 collaborateurs du groupe). Sous l'implusion du CEO et de la DRH, nos travaux portent sur 3 thématiques que sont : la collaboration, la performance et l’innovation. <br/> Prisma cherche à développer l’intrapreneuriat et favoriser une approche ascendante (bottom up). Dans ce contexte, je suis chargée d’un projet autour de l’innovation avec comme sponsor le CTO du groupe, également membre du comité de direction. </p>
